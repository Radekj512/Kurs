package pl.sda.kurs.z1_26_01_2019;

public class IncrementDecfement {
    public static void main(String[] args) {
        int x = 3;
        int y;

        System.out.println(x++);
        System.out.println(++x);
        System.out.println(x);
        y=x++;
        System.out.println(y);
        System.out.println(--y);
    }
}
