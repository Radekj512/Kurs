package pl.sda.kurs.z1_26_01_2019;

public class MinMaxValue {
    public static void main(String[] args) {
        System.out.println("Max wartosc dla int to: " + Integer.MAX_VALUE);
        System.out.println("Max wartosc dla int to: " + (Integer.MAX_VALUE+1));
        System.out.println("Min wartosc dla int to: " + Integer.MIN_VALUE);
        System.out.println("Max wartosc dla double to: " + Double.MAX_VALUE);
        System.out.println("Min wartosc dla double to: " + Double.MIN_VALUE);
        System.out.println("Min wartosc dla double to: " + Double.MIN_VALUE);
    }
}
